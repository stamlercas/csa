        <h3>Approval Process</h3>
        <ul>
            <li>
                The Clarion Students' Association (CSA) processes Fundraising Forms on a first-come, first-served basis. Please look at the Fundraising Tracking Sheet, posted in the "Documents" section of the Center for Leadership & Involvement's CU CONNECT page before you fill out this form to make sure there are no conflicts.
            </li>
            <li>
                Groups wishing to conduct a fundraiser must complete a Fundraising Form at least 5 business days* prior to the start of the event. The form will be reviewed by representatives of the CSA, as well as other applicable offices. If additional information is needed, the group will be notified. *If the group is conducting a raffle or a 50/50 drawing, the form must be completed at least 10 business days prior to the start of ticket sales.
            </li>
            <li>
                If the event requires space on campus, a completed space request must be on file with the Conference & Event Services Office, before the fundraiser can be approved.
            </li>
            <li>
                Assuming all requisite information is provided when the form is submitted, forms should be processed within 3 business days.
            </li>
            <li>
                At the completion of the fundraiser, all CSA funded organizations MUST file a Summary Report Form with the CSA Office. Future fundraising requests WILL NOT be approved until a Summary

                Report Form for this event has been filed.
            </li>
        </ul>
        <h3>GENERAL GUIDELINES</h3>
        <ul>
            <li>
                Fundraising Forms must be filled out using an RSO/RUO email account. Fundraising requests submitted using individual accounts will be denied.
            </li>
            <li>
                There is no limit to the number of fundraisers/collection drives an RSO can have per semester.
            </li>
            <li>
                Fundraisers must be approved BEFORE they are advertised or actually begin. This is to avoid having too many events of the same nature occurring at the same time. It is recommended that

                groups wishing to fundraise complete a Fundraising Form as soon as possible. However, the

                form must be completed <strong>at least 5 business days</strong> prior to the start of the fundraiser.
            </li>
            <li>
                There is a four-week limit on the duration of time to conduct a fundraiser. With over 140 groups on campus, this limit works to give all organizations a chance to conduct these types of

                activities. These timetables do not include vacation and breaks, therefore groups may wish to consider holding events that overlap vacations/breaks. However, raffles and 50/50 drawings will

                not be approved to be conducted during academic calendar breaks.
            </li>
            <li>
                At this time organizations will need to fill out separate Fundraising Forms each fundraising activity they conduct, even if they occur at the same event (i.e. holding a raffle during a dance where an admission fee is charged). We apologize for any inconvenience.
            </li>
        </ul>
        <h3>Raffles</h3>
        <div>
            In addition to being approved BEFORE they actually begin, raffles must be approved before the tickets are printed. A copy of the ticket proof must accompany the request and be approved before tickets are printed to avoid having organizations pay for printing tickets that they cannot use. The Clarion Students Association (CSA) purchases a Small Games of Chance License each year from the Clarion County Treasurer. Groups choosing to operate raffles must adhere to the following state requirements:
        </div>
        <ul>
            <li>
                Tickets must be preprinted, sequentially numbered and have a detachable stub with the same number (ticket rolls cannot be used for raffles). <strong>A copy of the ticket design must accompany the fundraising form.</strong>
            </li>
            <li>
                Tickets must have the name of the licensee (Clarion Students Association), the organization's name, ticket number, the date, time and location of the drawing, the license number (2697), the

                cost of the ticket and the available prize(s) (with the value of each prize) printed on the ticket

                side. Event name, purchaser's information (name, address & phone number), seller's name, CSA license number, and the matching ticket number must be on the detachable part of the ticket.
            </li>
            <li>
                All tickets must be brought to the CSA Office (278 Gemmell) PRIOR to distribution to members to sell. The CSA staff will work with the organization to create a log sheet which must be

                completed and returned to the CSA Office immediately following the conclusion of the raffle. If

                the group plans to sell raffle tickets outside Of Clarion County, they need to notify the CSA Office so it can be noted on the log sheet for auditing purposes.
            </li>
            <li>
                The drawing must be conducted in plain view of players present and winners need not be present. If the raffle is held during an event, a copy of the CSA Small Games of Chance permit must be displayed (a copy of the permit can be obtained from the CSA Office). If the drawing is not being held during an event, it must take place in the CSA Office, during regular business hours (8:30 a.m. -4:00 p.m.).
            </li>
            <li>
                The value of the raffle prize cannot exceed $2,000. If a raffle prize exceeds this single chance amount, a special permit must be obtained by CSA and requires 30 day advanced notification for

                the permit to be ordered. The organization requesting the special permit will also be responsible for the $25 special permit fee, which must be paid in advance of the order.
            </li>
            <li>
                The group must turn in all monies raised, the log sheet, all sold ticket stubs/unsold tickets and the winning ticket to the CSA Office and complete a summary report within two business days following the event. Groups failing to turn this documentation into CSA will be prohibited from conducting future raffles.
            </li>
        </ul>
        <h3>50/50 Drawings</h3>
        <ol>
            <li>
                All 50/50 drawings must be held on campus.
            </li>
            <li>
                50/50 drawings must registered at least 10 days prior to the event.
            </li>
            <li>
                The CSA will give organizations approved numbered 50/50 ticket rolls. Upon approval of your drawing, the member in charge of the event must stop into the CSA Office (278 Gemmell) to obtain the tickets. The CSA Office's normal business hours are 8:30 a.m. - 4:00 p.m., Monday

                through Friday.
            </li>
            <li>
                Tickets must be sold in set increments (I.e. 1/$1.00, 3/$5.00 or 5/$10.00, etc.), they cannot be sold at an "arm's length."
            </li>
            <li>
                Half of the money collected must be paid to the winning ticket holder. Members of the organization holding the drawing are not eligible to win.
            </li>
            <li>
                The name, address and phone of the winning ticket holder must be written on the back of the winning ticket.
            </li>
            <li>
                No more than $2,000.00 can be awarded at one time.
            </li>
            <li>
                The group must turn in all monies raised, the log sheet, all sold ticket stubs/unsold tickets and the winning ticket to the CSA Office and complete a summary report within two business days following the event. Groups failing to turn this documentation into CSA will be prohibited from conducting future raffles.
            </li>
        </ol>
        <h3>Bake Sales</h3>
        <div>
            If this food sale is a bake sale, the group must agree to abide by the following regulations out lined in PA

            Senate Bill 828 (a.k.a. the "Pie Bill"):
        </div>
        <ul>
            <li>
                Groups cannot sell "potentially hazardous foods" In the context of bake sales, "potentially hazardous foods" include cream pastries, cream/custard pies & tarts, pudding prepared from a

                mix or scratch, cream cheese and dairy whipped topping (Source: FDA Food Code 2005, Revised July 2007).
            </li>
            <li>
                Groups must individually wrap items for sale.
            </li>
            <li>
                Groups must post a sign at the bake sale informing consumers that the items for sale were prepared in private homes that are not licensed or inspected by PA Department of Agriculture.
            </li>
        </ul>
        <div>
